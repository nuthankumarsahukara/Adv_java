
public class StudentModel {
	private int std_id;
	private String std_name;
	private String std_course;
	private double std_fee;
	public int getStd_id() {
		return std_id;
	}
	public void setStd_id(int std_id) {
		this.std_id = std_id;
	}
	public String getStd_name() {
		return std_name;
	}
	public void setStd_name(String std_name) {
		this.std_name = std_name;
	}
	public String getStd_course() {
		return std_course;
	}
	public void setStd_course(String std_course) {
		this.std_course = std_course;
	}
	public double getStd_fee() {
		return std_fee;
	}
	public void setStd_fee(double std_fee) {
		this.std_fee = std_fee;
	}
}
